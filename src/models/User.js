import mongoose from 'mongoose';

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      default: 'admin',
    },
    status: {
      type: String,
      default: 'active',
    },
  },
  { timestamps: true }
);

export default mongoose.model('User', userSchema);
